export const getSuggestions = (query) => {
  if (!query || query.trim().length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();
  const queryWords = normalizedQuery
    .split(" ")
    .filter((word) => word.length > 0);
  const suggestions = [];

  serviceSuggestions.forEach((item) => {
    let score = 0;
    const matchedKeywords = [];

    item.keywords.forEach((keyword) => {
      const normalizedKeyword = keyword.toLowerCase();
      const keywordWords = normalizedKeyword.split(" ");

      // Exact match (highest priority)
      if (normalizedKeyword === normalizedQuery) {
        score += 10;
        matchedKeywords.push(keyword);
      }
      // Starts with query
      else if (normalizedKeyword.startsWith(normalizedQuery)) {
        score += 8;
        matchedKeywords.push(keyword);
      }
      // Contains full query as exact substring
      else if (normalizedKeyword.includes(normalizedQuery)) {
        score += 6;
        matchedKeywords.push(keyword);
      }
      // Query contains full keyword
      else if (normalizedQuery.includes(normalizedKeyword)) {
        score += 5;
        matchedKeywords.push(keyword);
      }
      // Check word by word matching (more sophisticated)
      else {
        let wordMatchScore = 0;
        queryWords.forEach((queryWord) => {
          keywordWords.forEach((keywordWord) => {
            // Keyword word starts with query word
            if (keywordWord.startsWith(queryWord)) {
              wordMatchScore += 3;
            }
            // Query word contains keyword word or vice versa
            else if (
              keywordWord.includes(queryWord) ||
              queryWord.includes(keywordWord)
            ) {
              wordMatchScore += 2;
            }
            // Partial match (at least 3 characters)
            else if (queryWord.length >= 3 && keywordWord.length >= 3) {
              if (keywordWord.substring(0, 3) === queryWord.substring(0, 3)) {
                wordMatchScore += 1;
              }
            }
          });
        });

        if (wordMatchScore > 0) {
          score += wordMatchScore;
          if (!matchedKeywords.includes(keyword)) {
            matchedKeywords.push(keyword);
          }
        }
      }
    });

    if (score > 0) {
      suggestions.push({
        service: item.service,
        score,
        matchedKeywords,
      });
    }
  });

  // Sort by relevance score (highest first)
  return suggestions.sort((a, b) => b.score - a.score).slice(0, 5); // Limit to top 5
};
